static int sprite_x;
static uint32_t sprite_frame;

void animated_sprites_c_init(void) {
    sprite_x = 0;
    sprite_frame = 0;
}

void animated_sprites_c_update(void) {
    uint32_t ticks = prg32_ticks_ms();
    sprite_x = (int)((ticks >> 3) & 255u);
    sprite_frame = prg32_sprite_anim_frame(ticks, 2, 160);
}

void animated_sprites_c_draw(void) {
    prg32_gfx_clear(PRG32_COLOR_BLACK);
    prg32_sprite_draw_frame(sprite_x,
                            88,
                            8,
                            8,
                            &frames[0][0],
                            sprite_frame,
                            PRG32_COLOR_WHITE);
    prg32_gfx_text8(8, 8, "SPRITES C", PRG32_COLOR_WHITE, PRG32_COLOR_BLACK);
}
