void prg32_abi_exports_keep(void) {
    volatile uintptr_t sink = 0;
    size_t count =
        sizeof(g_prg32_cart_abi_exports) / sizeof(g_prg32_cart_abi_exports[0]);
    for (size_t i = 0; i < count; ++i) {
        sink ^= (uintptr_t)g_prg32_cart_abi_exports[i];
    }
    (void)sink;
}
